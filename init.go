func js(v any) string {
	data, _ := json.Marshal(v)
	return string(data)
}

func writeplaylist(m Media, w io.Writer) error {
	init := ""
	m.File = append([]File{}, m.File...)
	for i := 0; i < len(m.File); i++ {
		f := &m.File[i]
		if init == f.Map.URI && !f.Discontinuous {
			f.Map.URI = ""
		} else {
			init = f.Map.URI
		}
	}
	tags, _ := m.EncodeTag()
	for _, t := range tags {
		fmt.Fprintln(w, t)
	}
	return nil
}

